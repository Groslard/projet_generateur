package modelParameter;

/**
 * The Class PrmParameter.
 */
public class PrmParameter {

	/** The name. */
	String name;

	/** The pakage name. */
	String pkg;

	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 *
	 * @param name
	 *            the new name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * Gets the pkg.
	 *
	 * @return the pkg
	 */
	public String getPkg() {
		return pkg;
	}

	/**
	 * Sets the pkg.
	 *
	 * @param pkg
	 *            the new pkg
	 */
	public void setPkg(String pkg) {
		this.pkg = pkg;
	}

}
