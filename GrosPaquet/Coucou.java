package GrosPaquet; 

public class Coucou { 

	private String nom; 
	private int numero; 

	public Coucou(){} 

	public void setNom(String nom){
		this.nom=nom;
	}

	public String getNom(){
		return nom;
	}

	public void setNumero(int numero){
		this.numero=numero;
	}

	public int getNumero(){
		return numero;
	}


 }